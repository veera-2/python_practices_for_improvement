# MATH FUNCTIONS
import math # you use this for any maths functions, it contains reusable maths functions. it use 'math.' to asign.

pi = 3.14
x = 1
y = 2
z = 3

print(round(pi))  # the (round()) function is use to round up a number to a given precision in decimal digits.
print(math.ceil(pi)) #
print(math.floor(pi)) #
print(abs(pi)) # return the absolute value of a number or an argument, it turns negetive to positive.
print(pow(pi, 2))  # it will raised the number to the power of a value you chose
print(math.sqrt(pi))  # will return the sqrt of a given number
print(max(x,y,z)) # use to fined the maximum value between any given numbers.
print(min(x,y,z)) # use to find the minimum value of the given numbers.
